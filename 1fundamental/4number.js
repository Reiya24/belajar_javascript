// melakukan perhitungan aritmatika ke tipe data number
console.log("melakukan perhitungan aritmatika ke tipe data number");
console.log(12 + 9);
console.log(12 - 9);
console.log(12 * 9);
console.log(12 / 9);
console.log(12 % 9);
console.log('===\n');

// increment decrement
console.log('postfix')
let postfix = 5;
console.log(postfix++);
console.log(postfix);
console.log('===\n');

console.log('prefix');
let prefix = 5;
console.log(++prefix);
console.log('===\n');

